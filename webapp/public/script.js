// Create contract instance
const contract = new web3.eth.Contract(contractABI, contractAddress);

// Update user's balance
async function updateBalance() {
  try {
      const accounts = await web3.eth.getAccounts();
      const balance = await contract.methods.balanceOf(accounts[0]).call();
      
      // Update UI or do something with the balance
      document.getElementById('balance').textContent = balance;
  } catch (error) {
      console.error("Error fetching balance:", error);
  }
}


// Send ETH for Tokens
document.getElementById('sendTokens').addEventListener('click', async () => {
    const sendAmount = document.getElementById('sendAmount').value;
    const accounts = await web3.eth.getAccounts();

    // Send transaction to contract function
    await contract.methods.sendEthForTokens().send({
        from: accounts[0],
        value: web3.utils.toWei(sendAmount, 'ether')
    });

    // Update balance after transaction
    updateBalance();
});

// Transfer Tokens
document.getElementById('transferTokens').addEventListener('click', async () => {
  const toAddress = document.getElementById('toAddress').value;
  const transferAmount = document.getElementById('transferAmount').value;
  const accounts = await web3.eth.getAccounts();

  await contract.methods.transferTokens(toAddress, transferAmount).send({
      from: accounts[0]
  });

  updateBalance();
});

//Send tokens back to the contract
document.getElementById('getEthBack').addEventListener('click', async () => {
  const tokenAmount = document.getElementById('tokenAmount').value;
  const accounts = await web3.eth.getAccounts();
  await contract.methods.getEthBack(tokenAmount).send({
      from: accounts[0]
  });
  updateBalance();
});

//Fetch token balance of an account
document.getElementById('checkAddressBalance').addEventListener('click', async () => {
  const addressToCheck = document.getElementById('addressToCheck').value;
  const tokenBalance = await contract.methods.checkTokenBalance(addressToCheck).call();
  document.getElementById('addressTokenBalance').textContent = tokenBalance;
});

// Initialize
window.ethereum.enable().then(() => {
  updateBalance();
});
